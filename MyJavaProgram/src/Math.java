
public class Math {
	
public static void main(String[] args) {
	 double x = 3.14;
	 double y = -10;
	 
	 double z = java.lang.Math.max(x, y); //min,max method
	 double j = java.lang.Math.abs(y); // abs method
	 double g = java.lang.Math.sqrt(z); //sqrt method
	 double d = java.lang.Math.round(z); //round method
	 double i = java.lang.Math.ceil(z);//ceil method
	 double k = java.lang.Math.floor(z); //floor method
	 System.out.println(z);
	 System.out.println(j);
	 System.out.println(g);
	 System.out.println(d);
	 System.out.println(i);
	 System.out.println(k);
}
}

